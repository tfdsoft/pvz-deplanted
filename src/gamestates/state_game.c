void state_game() {
    unsigned char statusbar_scroll = 0;
    unsigned char* palette_ptr;

    automatic_fs_updates = 0;
    
    //level = 0x20;

    unsigned char song = game_load_assets();
    
    if (level.world & 1) palette_ptr = (uint8_t*)pal_game_night;
    else palette_ptr = (uint8_t*)pal_game_day;

    flush_irq();
    
    // set statusbar chr
    add_interrupt(6, irq_set_chr);
        IRQ(0).arg0 = 0;
        IRQ(0).arg1 = 8;

    //__asm__("brk");

    // screen split
    add_interrupt(32, irq_update_bg_palette);
        IRQ(1).arg0 = ((uint16_t)palette_ptr & 0xff);
        IRQ(1).arg1 = ((uint16_t)palette_ptr >> 8);
        IRQ(1).arg3 = 49; // y position
        IRQ(1).arg4 = 2; // nametable number (0-3)

    // second screen split (for choosing seeds)
    add_interrupt(255, irq_update_bg_palette);
        IRQ(2).arg0 = ((uint16_t)pal_game_statusbar & 0xff);
        IRQ(2).arg1 = ((uint16_t)pal_game_statusbar >> 8);
        IRQ(2).arg2 = 0; // x position
        IRQ(2).arg3 = 54; // y position
        IRQ(2).arg4 = 0; // nametable number (0-3)
    add_interrupt(0,irq_set_chr);
        IRQ(3).arg0 = 0;
        IRQ(3).arg1 = 8;

    enable_irq();


    ppu_on_all();
    pal_bright(4);
    automatic_fs_updates = 1;



    music_play(song_choose_your_seeds);

    game_choose_seeds();
    
    
    music_play(song);

    while(1){
        ppu_wait_nmi();
        scroll(0,0);
        statusbar_scroll++;
        oam_clear();
        
        tick_statusbar(statusbar_scroll);

        

        if(player1_pressed & PAD_A){
            unsigned char song = song_win_music_dot_oh_gee_gee;
            if ((level.world == 4) && (level.stage == 10)) {
                song = song_final_fanfare_dot_oh_gee_gee;
            }
            music_play(song);
            for(short i=270; i>0; i--){
                ppu_wait_nmi();
            }
            gamestate = 0x10;
            pal_fade_to(4,8);
            break;
        }
    }
    flush_irq();
}
