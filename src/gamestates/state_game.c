    for(uint8_t cnt=0; cnt < 60; cnt++){
        ppu_wait_nmi();
        camera_speed += 0x08; 
        camera_scroll += camera_speed;
        IRQ(1).arg2 = high_byte(camera_scroll);
    }
    for(uint8_t cnt=0; cnt < 60; cnt++){
        ppu_wait_nmi();
        camera_speed -= 0x08; 
        camera_scroll += camera_speed;
        IRQ(1).arg2 = high_byte(camera_scroll);
    }

